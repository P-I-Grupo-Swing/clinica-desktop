    private void carregarTabela() {
        System.out.println("--- INICIANDO CARREGAMENTO DA TABELA ---");
        
        DefaultTableModel modelo = (DefaultTableModel) tblPacientes.getModel();
        modelo.setNumRows(0);

        try {
            List<Pacientes> lista = pacientesDao.listarTodos();
            
            System.out.println("Pacientes encontrados no banco: " + lista.size());

            for (Pacientes p : lista) {
                modelo.addRow(new Object[]{
                    p.getId(),
                    p.getNome(),
                    p.getCpf(),
                    p.getTelefone()
                });
                System.out.println("Adicionando na tela: " + p.getNome());
            }
        } catch (Exception e) {
            System.err.println("ERRO GRAVE AO CARREGAR: " + e.getMessage());
            e.printStackTrace();
        }
    }

