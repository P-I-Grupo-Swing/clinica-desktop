    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
    int linhaSelecionada = tblPacientes.getSelectedRow();
    
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um paciente na tabela para excluir.");
        return;
    }

    int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir?", "Exclusão", JOptionPane.YES_NO_OPTION);
    if (opcao == JOptionPane.YES_OPTION) {
        
        Long id = Long.valueOf(tblPacientes.getValueAt(linhaSelecionada, 0).toString());
        
        if (pacientesDao.excluir(id)) {
            JOptionPane.showMessageDialog(this, "Excluído com sucesso!");
            carregarTabela(); // Atualiza a tabela
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir.");
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnExcluirActionPerformed

