    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
int linha = tblAtendentes.getSelectedRow();
if (linha == -1) {
    JOptionPane.showMessageDialog(this, "Selecione um atendente para excluir.");
    return;
}

Long id = Long.valueOf(tblAtendentes.getValueAt(linha, 0).toString());

if (JOptionPane.showConfirmDialog(this, "Tem certeza?", "Excluir", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
    if (atendenteDao.excluir(id)) {
        JOptionPane.showMessageDialog(this, "Excluído!");
        carregarTabela();
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao excluir.");
    }
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnExcluirActionPerformed

