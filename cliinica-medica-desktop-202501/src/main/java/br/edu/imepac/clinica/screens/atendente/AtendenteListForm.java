    // 3. Método para buscar no banco e por na tabela
    private void carregarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblAtendentes.getModel();
        modelo.setNumRows(0); // Limpa

        List<Atendente> lista = atendenteDao.listarTodos();

        for (Atendente a : lista) {
            modelo.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getUsuario()
                // Não mostramos a senha por segurança
            });
        }
    }
