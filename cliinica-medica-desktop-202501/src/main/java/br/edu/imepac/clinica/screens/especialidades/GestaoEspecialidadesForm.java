    private void carregarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblEspecialidades.getModel();
        modelo.setNumRows(0);
        
        List<Especialidade> lista = dao.listarTodos();
        
        for (Especialidade e : lista) {
            modelo.addRow(new Object[]{
                e.getId(),
                e.getNome(),
                e.getDescricao()
            });
        }
    }

    private void limparCampos() {
        txtNome.setText("");
        txtDescricao.setText("");
        
        idSelecionado = null;
        tblEspecialidades.clearSelection();
        btnCadastrar.setEnabled(true);
        txtNome.requestFocus();
    }

    private Especialidade montarObjeto() {
        String nome = txtNome.getText();
        String desc = txtDescricao.getText();

        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nome é obrigatório!");
            return null;
        }

        Especialidade e = new Especialidade();
        e.setNome(nome);
        e.setDescricao(desc);
        return e;
    }
