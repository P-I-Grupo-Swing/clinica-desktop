    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
Especialidade e = montarObjeto();
        if (e != null) {
            if (dao.salvar(e)) {
                JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
                limparCampos();
                carregarTabela();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar.");
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
if (idSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Selecione na tabela.");
            return;
        }
        Especialidade e = montarObjeto();
        if (e != null) {
            e.setId(idSelecionado);
            if (dao.atualizar(e)) {
                JOptionPane.showMessageDialog(this, "Atualizado!");
                limparCampos();
                carregarTabela();
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
if (idSelecionado == null) return;
        if (JOptionPane.showConfirmDialog(this, "Excluir?", "Atenção", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            if (dao.excluir(idSelecionado)) {
                JOptionPane.showMessageDialog(this, "Excluído!");
                limparCampos();
                carregarTabela();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir (Pode estar em uso por um médico).");
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void tblEspecialidadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEspecialidadesMouseClicked
int linha = tblEspecialidades.getSelectedRow();
        if (linha != -1) {
            idSelecionado = Long.valueOf(tblEspecialidades.getValueAt(linha, 0).toString());
            txtNome.setText(tblEspecialidades.getValueAt(linha, 1).toString());
            txtDescricao.setText(tblEspecialidades.getValueAt(linha, 2).toString());
            btnCadastrar.setEnabled(false);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_tblEspecialidadesMouseClicked

