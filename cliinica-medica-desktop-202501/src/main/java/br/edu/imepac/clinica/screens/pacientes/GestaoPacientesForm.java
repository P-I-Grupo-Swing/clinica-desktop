    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
Paciente p = montarObjeto();
if (p != null) { // Só salva se a data estiver certa
    if (dao.salvar(p)) {
        JOptionPane.showMessageDialog(this, "Sucesso!");
        limparCampos();
        carregarTabela();
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao salvar.");
    }
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
// Verifica se clicou na tabela antes
        if (idSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Selecione um paciente na tabela para atualizar.");
            return;
        }

        // Pega os dados alterados dos campos
        Paciente p = montarObjeto();
        
        if (p != null) {
            p.setId(idSelecionado); // IMPORTANTE: Usa o ID capturado no clique!

            if (dao.atualizar(p)) {
                JOptionPane.showMessageDialog(this, "Atualizado com sucesso!");
                limparCampos();   // Limpa tudo
                carregarTabela(); // Atualiza a lista
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar.");
            }
        }  // TODO add your handling code here:
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void tblPacientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPacientesMouseClicked
// 1. Descobre qual linha foi clicada (0, 1, 2...)
        int linha = tblPacientes.getSelectedRow();

        if (linha != -1) {
            // 2. Pega o ID (que está escondido ou na coluna 0) e guarda na variável global
            // Isso é CRUCIAL para o botão 'Atualizar' saber quem alterar
            Object valorId = tblPacientes.getValueAt(linha, 0);
            this.idSelecionado = Long.valueOf(valorId.toString());

            // 3. Pega os dados das colunas e joga nos campos de texto
            // Nota: As colunas começam em 0. Verifique se a ordem bate com a sua tabela!
            
            // Coluna 1: Nome
            txtNome.setText(tblPacientes.getValueAt(linha, 1).toString());

            // Coluna 2: CPF (O campo formatado aceita o texto direto)
            txtCpf.setText(tblPacientes.getValueAt(linha, 2).toString());

            // Coluna 3: Data de Nascimento
            txtDataNascimento.setText(tblPacientes.getValueAt(linha, 3).toString());

            // Coluna 4: Telefone
            txtTelefone.setText(tblPacientes.getValueAt(linha, 4).toString());

            // 4. Trava o botão "Cadastrar" para evitar criar duplicado sem querer
            btnCadastrar.setEnabled(false);
        }
            // TODO add your handling code here:
    }//GEN-LAST:event_tblPacientesMouseClicked

