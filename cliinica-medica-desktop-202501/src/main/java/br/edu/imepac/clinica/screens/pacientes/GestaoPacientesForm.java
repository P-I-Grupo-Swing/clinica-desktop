    // --- Configura as máscaras (CPF, Data, Telefone) ---
    private void configurarMascaras() {
        try {
            // Data: 31/01/2022
            MaskFormatter maskData = new MaskFormatter("##/##/####");
            maskData.setPlaceholderCharacter('_');
            maskData.install(txtDataNascimento);

            // CPF: 111.111.111-11
            MaskFormatter maskCpf = new MaskFormatter("###.###.###-##");
            maskCpf.setPlaceholderCharacter('_');
            maskCpf.install(txtCpf);

            // Telefone: (34) 99999-9999
            MaskFormatter maskTel = new MaskFormatter("(##) #####-####");
            maskTel.setPlaceholderCharacter('_');
            maskTel.install(txtTelefone);
            
        } catch (Exception e) {
            System.err.println("Erro ao aplicar máscaras: " + e.getMessage());
        }
    }

    private void carregarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblPacientes.getModel();
        modelo.setNumRows(0);
        
        List<Paciente> lista = dao.listarTodos();
        
        for (Paciente p : lista) {
            // Formata a data para exibir bonito na tabela
            String dataBonita = "";
            if (p.getDataNascimento() != null) {
                dataBonita = p.getDataNascimento().format(formatter);
            }

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getCpf(),
                dataBonita, // Exibe dd/MM/yyyy
                p.getTelefone()
            });
        }
    }

   private void limparCampos() {
        // 1. Limpa o campo de texto normal
        txtNome.setText("");
        
        // 2. Limpa os campos formatados (O segredo é usar as duas linhas para cada um)
        txtCpf.setValue(null);
        txtCpf.setText(""); 
        
        txtDataNascimento.setValue(null);
        txtDataNascimento.setText("");
        
        txtTelefone.setValue(null);
        txtTelefone.setText("");
        
        // 3. Reseta o controle da tabela
        idSelecionado = null;
        tblPacientes.clearSelection(); 
        
        // 4. Reativa o botão cadastrar se estiver desligado
        btnCadastrar.setEnabled(true); 
        
        // 5. Põe o cursor no primeiro campo
        txtNome.requestFocus();
    }

    private Paciente montarObjeto() {
        Paciente p = new Paciente();
        p.setNome(txtNome.getText());
        p.setCpf(txtCpf.getText());
        p.setTelefone(txtTelefone.getText());
        
        // Tenta converter o texto da data para LocalDate
        try {
            LocalDate data = LocalDate.parse(txtDataNascimento.getText(), formatter);
            p.setDataNascimento(data);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data inválida! Preencha tudo: dia/mês/ano");
            return null; // Retorna nulo se der erro
        }
        return p;
    }
    
