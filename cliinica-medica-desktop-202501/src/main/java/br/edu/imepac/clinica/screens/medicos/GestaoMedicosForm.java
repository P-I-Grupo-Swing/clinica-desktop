    private void carregarComboEspecialidades() {
        // Busca todas as especialidades do banco
        List<Especialidade> lista = espDao.listarTodos();
        // Joga dentro do ComboBox (o toString da Especialidade mostra o nome)
        cbEspecialidade.setModel(new DefaultComboBoxModel(lista.toArray()));
    }

    private void carregarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblMedicos.getModel();
        modelo.setNumRows(0);
        
        List<Medico> lista = dao.listarTodos();
        
        for (Medico m : lista) {
            modelo.addRow(new Object[]{
                m.getId(),
                m.getNome(),
                m.getCrm(),
                m.getEspecialidade().getNome() // Mostra o nome da especialidade
            });
        }
    }

    private void limparCampos() {
        txtNome.setText("");
        txtCrm.setText("");
        txtSenha.setText("");
        
        // Volta o combo para o primeiro item
        if (cbEspecialidade.getItemCount() > 0) cbEspecialidade.setSelectedIndex(0);
        
        idSelecionado = null;
        tblMedicos.clearSelection();
        btnCadastrar.setEnabled(true);
        txtNome.requestFocus();
    }

    private Medico montarObjeto() {
        String nome = txtNome.getText();
        String crm = txtCrm.getText();
        String senha = new String(txtSenha.getPassword());
        
        // Pega o objeto Especialidade selecionado
        Especialidade esp = (Especialidade) cbEspecialidade.getSelectedItem();

        if (nome.isEmpty() || crm.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha Nome, CRM e Senha!");
            return null;
        }

        Medico m = new Medico();
        m.setNome(nome);
        m.setCrm(crm);
        m.setSenha(senha);
        m.setEspecialidade(esp);
        // Garante o ID para o DAO salvar a chave estrangeira corretamente
        if(esp != null) m.setEspecialidadeId(esp.getId());
        
        return m;
    }
