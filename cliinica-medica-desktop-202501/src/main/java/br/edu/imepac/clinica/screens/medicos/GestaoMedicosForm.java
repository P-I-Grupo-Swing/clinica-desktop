    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
Medico m = montarObjeto();
        if (m != null) {
            if (dao.salvar(m)) {
                JOptionPane.showMessageDialog(this, "Médico salvo com sucesso!");
                limparCampos();
                carregarTabela();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar.");
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
  if (idSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Selecione na tabela.");
            return;
        }
        Medico m = montarObjeto();
        if (m != null) {
            m.setId(idSelecionado);
            if (dao.atualizar(m)) {
                JOptionPane.showMessageDialog(this, "Atualizado!");
                limparCampos();
                carregarTabela();
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if (idSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Selecione um médico na tabela para deletar.");
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este médico?", "Atenção", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            
            if (dao.excluir(idSelecionado)) {
                JOptionPane.showMessageDialog(this, "Excluído com sucesso!");
                limparCampos();
                carregarTabela();
            } else {
                JOptionPane.showMessageDialog(this, "Não foi possível excluir.\n\n"
                        + "Motivo provável: Este médico já possui consultas agendadas.\n"
                        + "O sistema protege os dados para não perder o histórico.", 
                        "Erro ao Excluir", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void tblMedicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMedicosMouseClicked
int linha = tblMedicos.getSelectedRow();
        
        if (linha != -1) {
            // 1. Pega o ID da coluna 0
            idSelecionado = Long.valueOf(tblMedicos.getValueAt(linha, 0).toString());
            
            // 2. Busca o médico completo no banco
            Medico m = dao.buscarPorId(idSelecionado);
            
            if (m != null) {
                txtNome.setText(m.getNome());
                txtCrm.setText(m.getCrm());
                txtSenha.setText(m.getSenha());
                
                // 3. Seleciona a Especialidade na caixinha
                if (m.getEspecialidade() != null) {
                    // Aqui pegamos o ID do médico
                    long idDoMedico = m.getEspecialidade().getId();
                    
                    for (int i = 0; i < cbEspecialidade.getItemCount(); i++) {
                        Object item = cbEspecialidade.getItemAt(i);
                        
                        if (item instanceof Especialidade) {
                            Especialidade espDaCaixa = (Especialidade) item;
                            
                            // --- CORREÇÃO FINAL AQUI ---
                            // Compara direto, sem .longValue()
                            if (espDaCaixa.getId() == idDoMedico) {
                                cbEspecialidade.setSelectedIndex(i);
                                break; 
                            }
                        }
                    }
                }
            }
            btnCadastrar.setEnabled(false);
        }     // TODO add your handling code here:
    }//GEN-LAST:event_tblMedicosMouseClicked

