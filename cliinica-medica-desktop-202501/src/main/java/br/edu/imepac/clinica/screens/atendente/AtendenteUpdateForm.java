    // Método auxiliar para buscar no banco e pôr na tela
    private void carregarDados() {
        Atendente a = dao.buscarPorId(idAtendente);
        
        if (a != null) {
            txtNome.setText(a.getNome());
            txtUsuario.setText(a.getUsuario());
            txtSenha.setText(a.getSenha());
        } else {
            JOptionPane.showMessageDialog(this, "Erro: Atendente não encontrado.");
            this.dispose();
        }
    }
