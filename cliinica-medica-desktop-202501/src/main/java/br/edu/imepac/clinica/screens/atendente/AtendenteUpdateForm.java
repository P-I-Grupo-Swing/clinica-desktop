    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
// 1. Cria o objeto com os dados novos da tela
    Atendente a = new Atendente();
    a.setId(this.idAtendente); // IMPORTANTE: Usa o ID que guardámos no início
    a.setNome(txtNome.getText());
    a.setUsuario(txtUsuario.getText());
    a.setSenha(new String(txtSenha.getPassword()));

    // 2. Validação simples
    if (a.getNome().isEmpty() || a.getUsuario().isEmpty() || a.getSenha().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos os campos são obrigatórios.");
        return;
    }

    // 3. Chama o Atualizar no Banco
    if (dao.atualizar(a)) {
        JOptionPane.showMessageDialog(this, "Atendente atualizado com sucesso!");
        
        // Volta para a lista para ver a mudança
        new br.edu.imepac.clinica.screens.atendente.AtendenteListForm().setVisible(true);
        this.dispose(); // Fecha esta janela
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao atualizar. Verifique o console.");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnSalvarActionPerformed

