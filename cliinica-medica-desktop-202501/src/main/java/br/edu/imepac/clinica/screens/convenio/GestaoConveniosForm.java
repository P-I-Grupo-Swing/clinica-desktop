     private void configurarMascaras() {
        try {
            // CNPJ: 12.123.123/0001-00
            MaskFormatter maskCnpj = new MaskFormatter("##.###.###/####-##");
            maskCnpj.setPlaceholderCharacter('_');
            maskCnpj.install(txtCnpj);

            // Telefone: (34) 9999-9999
            MaskFormatter maskTel = new MaskFormatter("(##) ####-####");
            maskTel.setPlaceholderCharacter('_');
            maskTel.install(txtTelefone);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    // Preenche a tabela com dados do banco
    private void carregarTabela() {
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblConvenios.getModel();
        modelo.setNumRows(0);
        
        java.util.List<br.edu.imepac.clinica.entidades.Convenio> lista = dao.listarTodos();
        
        for (br.edu.imepac.clinica.entidades.Convenio c : lista) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNomeEmpresa(),
                c.getCnpj(),
                c.getTelefone(),
                c.getStatus()
            });
        }
    }

    // Limpa os campos e reseta a seleção
   private void limparCampos() {
        txtNome.setText("");
        txtCnpj.setValue(null);
        txtCnpj.setText("");
        txtTelefone.setValue(null);
        txtTelefone.setText("");
        
        if (cbStatus.getItemCount() > 0) {
            cbStatus.setSelectedIndex(0); 
        }
        
        idSelecionado = null;
        tblConvenios.clearSelection();
        btnCadastrar.setEnabled(true);
        txtNome.requestFocus();
    }

    // Cria o objeto Convenio a partir dos campos
   private Convenio montarObjeto() {
        String nome = txtNome.getText();
        String cnpj = txtCnpj.getText();
        String tel = txtTelefone.getText();
        Status status = (Status) cbStatus.getSelectedItem();

        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nome da empresa é obrigatório!");
            return null;
        }

        Convenio c = new Convenio();
        c.setNomeEmpresa(nome);
        c.setCnpj(cnpj);
        c.setTelefone(tel);
        c.setStatus(status);
        return c;
    }
    
