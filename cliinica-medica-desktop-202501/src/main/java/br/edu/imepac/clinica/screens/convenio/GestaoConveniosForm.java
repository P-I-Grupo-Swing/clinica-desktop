    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
if (idSelecionado == null) {
        JOptionPane.showMessageDialog(this, "Selecione para deletar.");
        return;
    }
    
    if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir?", "Excluir", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
        if (dao.excluir(idSelecionado)) {
            JOptionPane.showMessageDialog(this, "Excluído com sucesso!");
            limparCampos();
            carregarTabela();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir.");
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
Convenio c = montarObjeto();
    if (c != null) {
        if (dao.salvar(c)) {
            JOptionPane.showMessageDialog(this, "Convênio salvo com sucesso!");
            limparCampos();
            carregarTabela();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar.");
        }
    }      // TODO add your handling code here:
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
if (idSelecionado == null) {
        JOptionPane.showMessageDialog(this, "Selecione um convênio na tabela para atualizar.");
        return;
    }
    
    Convenio c = montarObjeto();
    if (c != null) {
        c.setId(idSelecionado); // Define qual ID atualizar

        if (dao.atualizar(c)) {
            JOptionPane.showMessageDialog(this, "Atualizado com sucesso!");
            limparCampos();
            carregarTabela();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar.");
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnAtualizarActionPerformed

