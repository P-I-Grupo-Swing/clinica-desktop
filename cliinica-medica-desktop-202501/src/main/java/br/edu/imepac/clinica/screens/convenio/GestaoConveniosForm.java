    private void tblConveniosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblConveniosMouseClicked
int linha = tblConvenios.getSelectedRow();
    if (linha != -1) {
        idSelecionado = Long.valueOf(tblConvenios.getValueAt(linha, 0).toString());
        
        txtNome.setText(tblConvenios.getValueAt(linha, 1).toString());
        txtCnpj.setText(tblConvenios.getValueAt(linha, 2).toString());
        txtTelefone.setText(tblConvenios.getValueAt(linha, 3).toString());
        
        Convenio c = dao.buscarPorId(idSelecionado);
        if(c != null) {
            cbStatus.setSelectedItem(c.getStatus());
        }
        
        btnCadastrar.setEnabled(false);
    }
    }//GEN-LAST:event_tblConveniosMouseClicked

