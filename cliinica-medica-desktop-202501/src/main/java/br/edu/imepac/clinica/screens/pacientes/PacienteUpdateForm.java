    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
// Validação
    if (txtNome.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nome é obrigatório.");
        return;
    }

    // Cria o objeto
    Pacientes p = new Pacientes();
    p.setId(this.pacienteId); // IMPORTANTE: Usa o ID que recebemos
    p.setNome(txtNome.getText());
    p.setRg(txtRg.getText());
    p.setCpf(txtCpf.getText());
    p.setTelefone(txtTelefone.getText());

    // Atualiza
    if (pacientesDao.atualizar(p)) {
        JOptionPane.showMessageDialog(this, "Atualizado com sucesso!");
        new PacienteListForm().setVisible(true); // Reabre a lista
        this.dispose(); // Fecha essa tela
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao atualizar.");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnSalvarActionPerformed

