    private void carregarDadosNoFormulario() {
        // Busca o paciente no banco pelo ID
        Pacientes p = pacientesDao.buscarPorId(this.pacienteId);
        
        if (p != null) {
            // Preenche os campos da tela
            txtNome.setText(p.getNome());
            txtRg.setText(p.getRg());
            txtCpf.setText(p.getCpf());
            txtTelefone.setText(p.getTelefone());
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao carregar paciente. ID não encontrado.");
            this.dispose();
        }
    }
