    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
if (txtHistorico.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Escreva pelo menos o histórico do paciente.");
            return;
        }

        // 1. Cria ou Atualiza o Prontuário
        // Verifica se já existe para decidir se é INSERT ou UPDATE
        Prontuario prontuarioExistente = prontuarioDao.buscarPorIdConsulta(idConsulta);
        boolean sucesso;

        if (prontuarioExistente == null) {
            // NOVO
            Prontuario novo = new Prontuario();
            novo.setHistorico(txtHistorico.getText());
            novo.setReceituario(txtReceituario.getText());
            novo.setExames(txtExames.getText());
            novo.setConsulta(consultaAtual); // Vincula à consulta
            sucesso = prontuarioDao.salvar(novo);
        } else {
            // ATUALIZAR EXISTENTE
            prontuarioExistente.setHistorico(txtHistorico.getText());
            prontuarioExistente.setReceituario(txtReceituario.getText());
            prontuarioExistente.setExames(txtExames.getText());
            sucesso = prontuarioDao.atualizar(prontuarioExistente);
        }

        if (sucesso) {
            // 2. Muda o status da consulta para REALIZADA
            consultaAtual.setStatus(StatusConsulta.REALIZADA);
            consultaDao.atualizar(consultaAtual);
            
            JOptionPane.showMessageDialog(this, "Atendimento finalizado com sucesso!");
            
            // Fecha esta tela e volta para a agenda (que precisa ser atualizada manualmente ou reaberta)
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar prontuário.");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnFinalizarActionPerformed

