    private void carregarDadosConsulta() {
        // Busca a consulta completa (com nome do paciente)
        this.consultaAtual = consultaDao.buscarPorId(idConsulta);
        
        if (consultaAtual != null) {
            lblNomePaciente.setText("Paciente: " + consultaAtual.getPaciente().getNome());
            
            // Verifica se já existia um prontuário salvo (modo edição)
            Prontuario p = prontuarioDao.buscarPorIdConsulta(idConsulta);
            if (p != null) {
                txtHistorico.setText(p.getHistorico());
                txtReceituario.setText(p.getReceituario());
                txtExames.setText(p.getExames());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao carregar consulta.");
            this.dispose();
        }
    }
